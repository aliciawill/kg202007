package 연산자;

public class 기본데이터확인문제1 {

	public static void main(String[] args) {
		int t = 66;
		int m = 77;
		int e = 88;
		int k = 99;
		
		int sum = t + m + e + k;
		int count = 4;
		double avg = (double)sum / count;
		System.out.println("평균은 " + avg);
		
		double pi = 3.14;
		double r = 2.2;
		double result = pi * r * r;
		System.out.println("원의 면적은 " + result);
	}
}




